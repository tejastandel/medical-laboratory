    private void btnRemoveTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveTestActionPerformed
        List<Integer> selectedRows = Arrays.stream(jTable1.getSelectedRows()).boxed().collect(Collectors.toList());
        Collections.sort(selectedRows, Collections.reverseOrder());
        for (Iterator<Integer> it = selectedRows.iterator(); it.hasNext();) {
            int index = it.next();
            model.removeRow(index);
        }
    }//GEN-LAST:event_btnRemoveTestActionPerformed

