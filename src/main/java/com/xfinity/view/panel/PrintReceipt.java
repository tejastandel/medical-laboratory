    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Receipt receipt = new Receipt();
        receipt.setPatientName(String.format("%S.%S", cmbTitle.getSelectedItem().toString(), txtName.getText()).replaceAll("\t+", " ").replaceAll(" +", " ").trim());
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        receipt.setDate(simpleDateFormat.format(dateChooser.getDate()));
        receipt.setAge(String.format("%s %s", txtAge.getText().trim(), rBtnYears.isSelected() ? "Year(s)" : "Month(s)"));
        receipt.setGender(cmbGender.getSelectedItem().toString());
        List<ReceiptTest> results = new ArrayList<>();
        Vector<Vector> dataVector = model.getDataVector();
        ReceiptTest result;
        for (Vector vector : dataVector) {
            results.add(result = new ReceiptTest());
            result.setTestName(vector.elementAt(0).toString());
            result.setAmount(Double.parseDouble(vector.elementAt(1).toString()));
            result.setDiscount(Double.parseDouble(vector.elementAt(2).toString()));
            result.setQty(Integer.parseInt(vector.elementAt(3).toString()));
        }
        receipt.setReceiptTests(results);
        try {
            PrintManager.printReceipt(receipt);
        } catch (JRException | FileNotFoundException | ParseException ex) {
            Logger.getLogger(AddTestResult.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void cmbTitleItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbTitleItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            Title title = (Title) evt.getItem();
            if (title.gender != Gender.UNKNOWN) {
                cmbGender.setSelectedIndex(title.gender.VALUE);
                cmbGender.setEnabled(false);
            } else {
                cmbGender.setEnabled(true);
            }
        }
    }//GEN-LAST:event_cmbTitleItemStateChanged

